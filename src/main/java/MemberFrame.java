    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
    Connection conn = Koneksi.getConnection();

    String sqlPengguna = "INSERT INTO pengguna(nama, email, no_telp) VALUES (?, ?, ?)";
    PreparedStatement pstPengguna = conn.prepareStatement(
            sqlPengguna,
            PreparedStatement.RETURN_GENERATED_KEYS
    );

    pstPengguna.setString(1, jTextField5.getText());
    pstPengguna.setString(2, jTextField6.getText());
    pstPengguna.setString(3, jTextField7.getText());
    pstPengguna.executeUpdate();

    ResultSet rsPengguna = pstPengguna.getGeneratedKeys();

    int idPengguna = 0;
    if (rsPengguna.next()) {
        idPengguna = rsPengguna.getInt(1);
    }

    String sqlMembership = "SELECT id_membership FROM membership WHERE tipe=?";
    PreparedStatement pstMembership = conn.prepareStatement(sqlMembership);
    pstMembership.setString(1, jComboBox1.getSelectedItem().toString());

    ResultSet rsMembership = pstMembership.executeQuery();

    int idMembership = 0;
    if (rsMembership.next()) {
        idMembership = rsMembership.getInt("id_membership");
    }

    String sqlMember = "INSERT INTO member "
            + "(tanggal_daftar, status_aktif, id_pengguna, id_membership) "
            + "VALUES (?, ?, ?, ?)";

    PreparedStatement pstMember = conn.prepareStatement(sqlMember);

    pstMember.setString(1, jTextField8.getText());
    pstMember.setString(2, jTextField1.getText());
    pstMember.setInt(3, idPengguna);
    pstMember.setInt(4, idMembership);

    pstMember.executeUpdate();

    JOptionPane.showMessageDialog(this, "Data member berhasil ditambahkan");

    tampilData();
    resetForm();

} catch (HeadlessException | SQLException e) {
    JOptionPane.showMessageDialog(this, "Gagal tambah data: " + e.getMessage());
}        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

