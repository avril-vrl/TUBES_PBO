    private void isiComboMembership() {
    try {
        jComboBox1.removeAllItems();

        String sql = "SELECT tipe FROM membership";
        Connection conn = Koneksi.getConnection();
        PreparedStatement pst = conn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();

        while (rs.next()) {
            jComboBox1.addItem(rs.getString("tipe"));
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
}
    private void tampilData() {
    DefaultTableModel model = new DefaultTableModel();

    model.addColumn("ID");
    model.addColumn("Nama");
    model.addColumn("Email");
    model.addColumn("No Telp");
    model.addColumn("Tanggal Daftar");
    model.addColumn("Status");
    model.addColumn("Membership");

    try {
        String sql =
                "SELECT m.nomor_member, p.nama, p.email, p.no_telp, "
                + "m.tanggal_daftar, m.status_aktif, ms.tipe "
                + "FROM member m "
                + "JOIN pengguna p ON m.id_pengguna = p.id "
                + "JOIN membership ms ON m.id_membership = ms.id_membership";

        Connection conn = Koneksi.getConnection();
        PreparedStatement pst = conn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("nomor_member"),
                rs.getString("nama"),
                rs.getString("email"),
                rs.getString("no_telp"),
                rs.getDate("tanggal_daftar"),
                rs.getString("status_aktif"),
                rs.getString("tipe")
            });
        }

        jTable1.setModel(model);

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
}
