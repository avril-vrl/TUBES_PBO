@Override
public void tampilData() {
    
    DefaultTableModel model = new DefaultTableModel();

    model.addColumn("ID");
    model.addColumn("Tipe");
    model.addColumn("Harga");
    model.addColumn("Tanggal Mulai");
    model.addColumn("Tanggal Berakhir");
    model.addColumn("Durasi Hari");

    try {

        String sql = "SELECT * FROM membership";

        Connection conn = Koneksi.getConnection();

        PreparedStatement pst =
                conn.prepareStatement(sql);

        ResultSet rs =
                pst.executeQuery();

        while (rs.next()) {

            model.addRow(new Object[]{

                rs.getInt("id_membership"),
                rs.getString("tipe"),
                String.format("%.0f", rs.getDouble("harga")),
                rs.getDate("tanggal_mulai"),
                rs.getDate("tanggal_berakhir"),
                rs.getInt("durasi_hari")

            });

        }

        jTable1.setModel(model);

    } catch (SQLException e) {

        JOptionPane.showMessageDialog(
                this,
                e.getMessage()
        );

    }

}
