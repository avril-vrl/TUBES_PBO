    String sql =
        "UPDATE membership SET "
        + "tipe=?, "
        + "harga=?, "
        + "tanggal_mulai=?, "
        + "tanggal_berakhir=?, "
        + "durasi_hari=? "
        + "WHERE id_membership=?";

    Connection conn = Koneksi.getConnection();

    PreparedStatement pst =
            conn.prepareStatement(sql);

    pst.setString(1, jTextField2.getText());
    pst.setDouble(2, Double.parseDouble(jTextField3.getText()));
