    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    try {

        Connection conn = Koneksi.getConnection();

        // Simpan ke tabel pengguna
        String sqlPengguna =
                "INSERT INTO pengguna(nama,email,no_telp) "
                + "VALUES(?,?,?)";

        PreparedStatement pstPengguna =
                conn.prepareStatement(
                        sqlPengguna,
                        PreparedStatement.RETURN_GENERATED_KEYS
                );

        pstPengguna.setString(1, jTextField2.getText());
        pstPengguna.setString(2, jTextField3.getText());
        pstPengguna.setString(3, jTextField4.getText());

        pstPengguna.executeUpdate();

        // Ambil ID pengguna otomatis
        ResultSet rs =
                pstPengguna.getGeneratedKeys();

        int idPengguna = 0;

        if(rs.next()){
            idPengguna = rs.getInt(1);
        }

        // Simpan ke tabel trainer
        String sqlTrainer =
                "INSERT INTO trainer "
                + "(id_pengguna, spesialisasi, gaji_per_jam, rating) "
                + "VALUES(?,?,?,?)";

        PreparedStatement pstTrainer =
                conn.prepareStatement(sqlTrainer);

        pstTrainer.setInt(1, idPengguna);
        pstTrainer.setString(2, jTextField5.getText());
        pstTrainer.setDouble(3,
                Double.parseDouble(jTextField6.getText()));
        pstTrainer.setDouble(4,
                Double.parseDouble(jTextField7.getText()));

        pstTrainer.executeUpdate();

        JOptionPane.showMessageDialog(
                this,
                "Data trainer berhasil ditambahkan"
        );

        tampilData();
        resetForm();

    } catch (HeadlessException | NumberFormatException | SQLException e) {

        JOptionPane.showMessageDialog(
                this,
                e.getMessage()
        );

    }

    }//GEN-LAST:event_jButton1ActionPerformed

