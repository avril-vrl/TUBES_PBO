    private void tampilData() {

    DefaultTableModel model = new DefaultTableModel();

    model.addColumn("ID");
    model.addColumn("Nama");
    model.addColumn("Email");
    model.addColumn("No Telp");
    model.addColumn("Spesialisasi");
    model.addColumn("Gaji Per Jam");
    model.addColumn("Rating");

    try {

        String sql =
                "SELECT t.id_trainer, p.nama, p.email, p.no_telp, "
                + "t.spesialisasi, t.gaji_per_jam, t.rating "
                + "FROM trainer t "
                + "JOIN pengguna p ON t.id_pengguna = p.id";

        Connection conn = Koneksi.getConnection();

        PreparedStatement pst =
                conn.prepareStatement(sql);

        ResultSet rs =
                pst.executeQuery();

        while (rs.next()) {

            model.addRow(new Object[]{

                rs.getInt("id_trainer"),
                rs.getString("nama"),
                rs.getString("email"),
                rs.getString("no_telp"),
                rs.getString("spesialisasi"),
                String.format("%.0f", rs.getDouble("gaji_per_jam")),
                rs.getDouble("rating")

            });

        }

        jTable1.setModel(model);

    } catch (SQLException e) {

        JOptionPane.showMessageDialog(
                this,
                e.getMessage()
        );

    }

}
